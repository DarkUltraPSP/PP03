package com.crays.gmagro.models;

public class Site {
    private String uai;
    private String nom;
    private String adr;
    private String cp;
    private String ville;
    private double lat;
    private double lng;

    public String getUai() {
        return uai;
    }

    public String getNom() {
        return nom;
    }

    public String getAdr() {
        return adr;
    }

    public String getCp() {
        return cp;
    }

    public String getVille() {
        return ville;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }
}
